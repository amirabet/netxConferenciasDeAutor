  <div itemprop="description">
    Usain Bolt is a Jamaican sprinter widely regarded as the fastest person ever. 
    He is the first man to hold both the 100 metres and 200 metres world records since 
    fully automatic time measurements became mandatory in 1977. Along with his teammates, 
    he also set the world record in the 4x100 metres relay. He is the reigning Olympic champion 
    in these three events, the first man to win six Olympic gold medals in sprinting, 
    and a five-time World champion. He was the first to achieve a "double double" by winning 
    100m and 200m titles at consecutive Olympics (2008 and 2012), and topped this through 
    the first "double triple" (including 4x100m relays).
